package Day16;

public class NestedLoopTask1 {
    public static void main(String[] args) {

        //task
        // print grid of# of 10 rowws 10 columns


        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.print("#");
            }

            System.out.println();

        }
    }
}


